// 备注信息     只在"main.c"中调用一次
//-------------------------------------------------------------------------------------------------------------------
void Peripheral_Init(void)
{
    // IPS200 初始化
    ips200_init("/dev/fb0");
    ips200_clear();
//    ips200_full(RGB565_BLACK);

    // 电机初始化调用
    Motor_Init();

    // IMU 初始化
    imu_get_dev_info();

    // 初始化UVC摄像头
    if(uvc_camera_init("/dev/video0") < 0)
    {
        printf("failed:UVC初始化失败");
    }

    if (!ld_usart_init("/dev/ttyUSB0", 230400))
        {
        printf("failed:USB0初始化失败");
    }
}

