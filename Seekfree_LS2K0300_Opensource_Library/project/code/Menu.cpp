int Avoid_Mode(void)
{
    Avoid_UI();

    Mode_Flag = AVOID_MODE;

    while(1)
    {
        /* 按键处理*/
        if (Key_Check(KEY_NAME_BACK,KEY_SINGLE))
        {
            Mode_Flag = IDLE_MODE;
            // 返回上一级界面
            return 0;   
        }

        

    }
}
